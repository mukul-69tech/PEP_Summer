/*
Time complexity = how much time an algorithm takes as input size increases
1. big O - represents the upper bound of running time, it gives the worst case scenario for an algorithm, it describes the maximum time am algorithm can take

2. O(1) -> constant time  -> the time taken doesn't depend on the input size, algorithm always runs in the same time

for example :  cout << "Hello world "
               for(int i=0;i<5;i++){
               }

3. O(n) -> linear time -> the time grows directly proportional to input size


4. O(logn) -> logarthmic time -> the time grows logarthmically as the input size increases, often seen in algos that divide the input in half like binary search

5. O(nlogn) -> linearithmic -> mix of linear and logarithmic, quick sort and merge sort

6. O(n^2) -> quadratic -> time will grow proportionally to the square of input size

*/